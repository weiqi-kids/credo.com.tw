#!/usr/bin/env node
// 法律主張地雷掃描（律師 2026-07-14 查核報告歸納的錯誤類型）。
// 用法：node scripts/audit-legal-claims.mjs　　※語意型檢查，命中=需人工覆核，非硬性 gate
import { readFileSync, readdirSync } from "node:fs";
const PATTERNS = [
  ["1.34倍寫成法定", /1\.34 ?倍(?!，避免|計算)/],
  ["責任制絕對化", /責任制.{0,30}(排除加班費|不用給加班費)|核備.{0,20}才能排除加班費/],
  ["月薪含加班費絕對無效", /(?<!以為「)月薪已含加班費」?的約定(?!一定無效，也有人)(.{0,20})(一定無效|不會承認)/],
  ["拒開證明錯誤法源", /(就業保險法|就保法).{0,30}(拒開|拒發).{0,20}罰/],
  ["拋棄繼承通知歸法院", /法院會通知其他繼承人/],
  ["限定繼承改用論", /(改用|只能用).{0,8}限定繼承/],
  ["資遣事由逾一個月", /暫停工作逾一個月/],
  ["2年贈與未限對象", /死亡前 ?2 ?年內?贈與(給)?(?!.{0,40}(及其配偶|其配偶|視為遺產的地雷|，仍會被視為))/],
  ["遺產稅延長絕對上限", /延長.{0,10}(最長|最多).{0,6}3 ?個月(?!為限)/],
  ["口頭協議不被承認", /口頭(協議|喬定).{0,20}(不被承認|(?<!不是)當然無效)/],
  ["保險金無實質課稅但書", /保險金.{0,20}不(算|列入|計入).{0,8}遺產(?![\s\S]{0,500}實質課稅)/],
  ["生前贈與扣減過寬", /生前贈與.{0,20}(被請求扣減|被扣減)/],
  ["調解20日內開會", /20 ?日內.{0,6}召開.{0,8}調解/],
];
let hits = 0;
for (const dir of ["src/content/insights", "src/content/resources"]) {
  for (const f of readdirSync(dir).filter((x) => x.endsWith(".md"))) {
    // 略過「資料來源」段——條文標籤（如「死亡前2年內贈與之併計」）非主張本身
    const s = readFileSync(`${dir}/${f}`, "utf8").split(/^## 資料來源$/m)[0];
    for (const [label, re] of PATTERNS) {
      const m = s.match(re);
      if (m) { console.log(`⚠ ${f}｜${label}｜…${m[0].slice(0, 50)}…`); hits += 1; }
    }
  }
}
console.log(hits ? `${hits} 處需人工覆核` : "法律主張掃描：無已知地雷模式。");
process.exit(0);
